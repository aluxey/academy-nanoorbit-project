CREATE TABLE ORBITES (
  ID_ORBITE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TYPE_ORBITE VARCHAR2(20) NOT NULL,
  ALTITUDE_KM NUMBER(8,2) NOT NULL,
  INCLINAISON_DEG NUMBER(6,2) NOT NULL,
  ZONE_COUVERTE VARCHAR2(100) NOT NULL
);

CREATE TABLE SATELLITES (
  ID_SATELLITE VARCHAR2(20) PRIMARY KEY,
  NOM VARCHAR2(100) NOT NULL,
  STATUT VARCHAR2(20) NOT NULL,
  FORMAT_CUBESAT VARCHAR2(10) NOT NULL,
  ID_ORBITE NUMBER NOT NULL,
  DATE_LANCEMENT DATE,
  MASSE_KG NUMBER(8,2),
  CONSTRAINT FK_SAT_ORB FOREIGN KEY (ID_ORBITE) REFERENCES ORBITES(ID_ORBITE)
);

CREATE TABLE STATIONS_SOL (
  CODE_STATION VARCHAR2(20) PRIMARY KEY,
  NOM VARCHAR2(100) NOT NULL,
  LATITUDE NUMBER(10,6) NOT NULL,
  LONGITUDE NUMBER(10,6) NOT NULL
);

CREATE TABLE FENETRES_COM (
  ID_FENETRE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  DATETIME_DEBUT TIMESTAMP NOT NULL,
  DUREE NUMBER(6) NOT NULL,
  STATUT VARCHAR2(20) NOT NULL,
  ID_SATELLITE VARCHAR2(20) NOT NULL,
  CODE_STATION VARCHAR2(20) NOT NULL,
  VOLUME_DONNEES NUMBER(10,2),
  CONSTRAINT FK_FEN_SAT FOREIGN KEY (ID_SATELLITE) REFERENCES SATELLITES(ID_SATELLITE),
  CONSTRAINT FK_FEN_STATION FOREIGN KEY (CODE_STATION) REFERENCES STATIONS_SOL(CODE_STATION)
);

CREATE TABLE ANOMALIES (
  ID_ANOMALIE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_SATELLITE VARCHAR2(20) NOT NULL,
  DATE_SIGNALEMENT TIMESTAMP NOT NULL,
  DESCRIPTION VARCHAR2(1000) NOT NULL,
  STATUT VARCHAR2(20) NOT NULL,
  CONSTRAINT FK_ANOM_SAT FOREIGN KEY (ID_SATELLITE) REFERENCES SATELLITES(ID_SATELLITE)
);

CREATE INDEX IDX_FENETRES_DEBUT ON FENETRES_COM (DATETIME_DEBUT);
CREATE INDEX IDX_ANOMALIES_SAT_DATE ON ANOMALIES (ID_SATELLITE, DATE_SIGNALEMENT DESC);
